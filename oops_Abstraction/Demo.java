package oops_Abstraction;

public abstract class Demo {

	void m1() {  // concrete method
		System.out.println("concrete method m1");
	}

	void m2() { // concrete method
		System.out.println("concrete method m2");
	}
		/* Important interview question=	
		 * Que : What are the possible abstraction possible conditions for a abstract class?
		 * Ans:  abstract class possible conditions are,
			1) we can have only concrete methods in abstract class
			2) we can have abstract methods & concrete methods in a abstract class
			3) we can have only abstract methods in a abstract class
			 */
}
