package arrays;

public class Array2 {

	public static void main(String[] args) {

		int ar[] = new int[5];// 1st Approach of creating of an array with integer datatype

		ar[0] = 10;
		ar[1] = 11;
		ar[2] = 25;
		ar[3] = 20;
		ar[4] = 15;

		for (int i = 0; i <= ar.length - 1; i++) {

			System.out.println(ar[i]);

		}

	}

}
